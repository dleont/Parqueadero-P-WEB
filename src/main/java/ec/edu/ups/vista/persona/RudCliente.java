    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            persona.setCedula(txtBuscar.getText());
            personaDAO.delete(persona);
                
            JOptionPane.showMessageDialog(this, "Cliente eliminado con éxito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            txtBuscar.setText("");
            txtBCedula.setText("");
            txtBNombre.setText("");
            txtBApellido.setText("");
            txtANombre.setText("");
            txtAApellido.setText("");
            comboGenero.removeAllItems();
            txtBuscar.setEditable(true);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "La persona que desea eliminar no existe", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String cedu = txtBCedula.getText();
      
        if (cedu.isEmpty() ) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese valores para Actualizar", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            //cliente.setCodigo(Integer.valueOf(clienteA.getCodigo()));//
            persona.setCedula(txtBCedula.getText());
            persona.setNombre(txtANombre.getText());
            persona.setApellido(txtAApellido.getText());
            
            personaDAO.update(persona);
            JOptionPane.showMessageDialog(this, " Datos Actualizados", "Ventana Actualizar", JOptionPane.OK_OPTION);

            txtBuscar.setText("");
            txtBCedula.setText("");
            txtBNombre.setText("");
            txtBApellido.setText("");
            txtBuscar.setEditable(true);
            txtANombre.setText("");
            txtAApellido.setText("");
            txtGenero.setText("");
            comboGenero.removeAllItems();
        }
        txtBuscar.requestFocus();

    }//GEN-LAST:event_btnModificarActionPerformed


