    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         try {
             //para eliminar
            persona = personaDAO.buscar(txtBuscar.getText());
            txtBCedula.setText(persona.getCedula());
            txtBNombre.setText(persona.getNombre());
            txtBApellido.setText(persona.getApellido());
            txtGenero.setText(persona.getGenero());
            
            //para actualizar datos
            txtANombre.setText(persona.getNombre());
            txtAApellido.setText(persona.getApellido());
            comboGenero.addItem(persona.getGenero()); // llenando jcombobox
            txtBuscar.setEditable(false);
            
        }catch(java.lang.NullPointerException ex){
            JOptionPane.showMessageDialog(this, "El cliente no existe", "Error", JOptionPane.WARNING_MESSAGE);
            txtBuscar.setText("");
            txtBuscar.setEditable(true);
            txtBCedula.setText("");
            txtBNombre.setText("");
            txtBApellido.setText("");
            txtANombre.setText("");
            txtAApellido.setText("");
            txtGenero.setText("");
            comboGenero.removeAllItems();
        }
        txtBuscar.requestFocus();
    }//GEN-LAST:event_btnBuscarActionPerformed

